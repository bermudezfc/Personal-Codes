package Rough;


public class SystemData{
	private String roomNum;
	private String computerNum;
	private String problem;
	private String issue;
	private String submitteeID;
	
	public String getRoomNum() {
		return roomNum;
	}
	public void setRoomNum(String roomNum) {
		this.roomNum = roomNum;
	}
	public String getComputerNum() {
		return computerNum;
	}
	public void setComputerNum(String computerNum) {
		this.computerNum = computerNum;
	}
	public String getProblem() {
		return problem;
	}
	public void setProblem(String problem) {
		this.problem = problem;
	}
	public String getIssue() {
		return issue;
	}
	public void setIssue(String issue) {
		this.issue = issue;
	}
	public String getSubmitteeID() {
		return submitteeID;
	}
	public void setSubmitteeID(String submitteeID) {
		this.submitteeID = submitteeID;
	}
}